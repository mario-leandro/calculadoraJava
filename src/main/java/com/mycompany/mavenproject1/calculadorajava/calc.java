    private void jBmmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBmmActionPerformed
        if (temp.startsWith("-")){
            temp = temp.replace('-','+');
        } else if (temp.startsWith("+")) {
            temp = temp.replace('+','-');
        } else{
            temp = "+" + temp;
        }
        
        jTVisor.setText(temp);
    }//GEN-LAST:event_jBmmActionPerformed

    private void jBigualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBigualActionPerformed
         num2 = Double.parseDouble(jTVisor.getText());
            switch (operacao) {
                case '+': 
                    resultado = num1 + num2; 
                    break;
                case '-':
                    resultado = num1 - num2; 
                    break;
                case 'X':
                    resultado = num1 * num2; 
                    break;
                case '/':
                    resultado = num1 / num2; 
                    break;
        } 
        jTVisor.setText(String.valueOf(resultado));
        numeroDigitado("2");
    }//GEN-LAST:event_jBigualActionPerformed

